// ✅ FUNCIÓN PARA APLICAR FILTROS LOCALES
export const applyLocalFilters = (vehicles, filters) => {
    return vehicles.filter(vehicle => {
        // ✅ FILTRO POR MARCA
        if (filters.marca && !vehicle.marca.toLowerCase().includes(filters.marca.toLowerCase())) {
            return false
        }
        
        // ✅ FILTRO POR MODELO
        if (filters.modelo && !vehicle.modelo.toLowerCase().includes(filters.modelo.toLowerCase())) {
            return false
        }
        
        // ✅ FILTRO POR PRECIO
        if (filters.precioMin && vehicle.precio < filters.precioMin) {
            return false
        }
        if (filters.precioMax && vehicle.precio > filters.precioMax) {
            return false
        }
        
        // ✅ FILTRO POR AÑO
        if (filters.añoMin && vehicle.año < filters.añoMin) {
            return false
        }
        if (filters.añoMax && vehicle.año > filters.añoMax) {
            return false
        }
        
        // ✅ FILTRO POR KILOMETRAJE
        if (filters.kilometrosMin && vehicle.kms < filters.kilometrosMin) {
            return false
        }
        if (filters.kilometrosMax && vehicle.kms > filters.kilometrosMax) {
            return false
        }
        
        // ✅ FILTRO POR TRANSMISIÓN
        if (filters.transmisión && vehicle.transmisión !== filters.transmisión) {
            return false
        }
        
        // ✅ FILTRO POR COMBUSTIBLE
        if (filters.combustible && vehicle.combustible !== filters.combustible) {
            return false
        }
        
        // ✅ FILTRO POR COLOR
        if (filters.color && vehicle.color !== filters.color) {
            return false
        }
        
        // ✅ FILTRO GENERAL DE BÚSQUEDA
        if (filters.search) {
            const searchTerm = filters.search.toLowerCase()
            const searchableFields = [
                vehicle.marca,
                vehicle.modelo,
                vehicle.version,
                vehicle.color,
                vehicle.categoria,
                vehicle.detalle
            ].join(' ').toLowerCase()
            
            if (!searchableFields.includes(searchTerm)) {
                return false
            }
        }
        
        return true
    })
}

